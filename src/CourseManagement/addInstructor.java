    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        
        idInstructor.setText(model.getValueAt(selectedRow, 0).toString());
        firstNameInstructor.setText(model.getValueAt(selectedRow, 1).toString());
        lastNameInstructor.setText(model.getValueAt(selectedRow, 2).toString());
        genderInstructor.setSelectedItem(model.getValueAt(selectedRow, 3).toString());
        phoneInstructor.setText(model.getValueAt(selectedRow, 4).toString());
        salaryInstructor.setText(model.getValueAt(selectedRow, 5).toString().replace("EGP ",""));
    }//GEN-LAST:event_jTable1MouseClicked
public void showInstructorTable(){
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/coursesmanagement","root","");
            String sql = "SELECT * FROM manageinstructors";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in displaying data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
    }
