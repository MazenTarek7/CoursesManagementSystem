    private void insertInstructorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertInstructorActionPerformed
        if (firstNameInstructor.getText().isBlank() == true || lastNameInstructor.getText().isBlank() == true|| phoneInstructor.getText().isBlank() == true
                || idInstructor.getText().isBlank() == false || salaryInstructor.getText().isBlank() == true){
            JOptionPane.showMessageDialog(null, "Some data fields are empty or id field is written into , while its autogenerated"
                    , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        else{
        try {
            String sql = "INSERT INTO manageinstructors"
                    +"(FirstName, LastName, Gender, Phone, Salary)"
                    +"VALUES (?,?,?,?,?)";
            con = DriverManager.getConnection("jdbc:mysql://localhost/coursesmanagement","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1, firstNameInstructor.getText());
            pst.setString(2, lastNameInstructor.getText());
            pst.setString(3, String.valueOf(genderInstructor.getSelectedItem()));
            pst.setString(4, phoneInstructor.getText());
            pst.setString(5, "EGP "+salaryInstructor.getText());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null , "Data inserted successfully");
            
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in inserting data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showInstructorTable();
        }
    }//GEN-LAST:event_insertInstructorActionPerformed

    private void deleteInstructorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteInstructorActionPerformed
        try {
            String sql = "DELETE FROM manageinstructors WHERE ID = ?";
            con = DriverManager.getConnection("jdbc:mysql://localhost/coursesmanagement","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1, idInstructor.getText());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null , "Data deleted successfully");
            
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in deleting data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showInstructorTable();
    }//GEN-LAST:event_deleteInstructorActionPerformed

