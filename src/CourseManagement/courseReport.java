    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (startDate.getText().isBlank() == true || endDate.getText().isBlank() == true || jComboBox1.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Some data fields are empty"
                    , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        else{
        try {
            String sql = "INSERT INTO coursereport"
                    +"(Course_ID,startDate, endDate)"
                    +"VALUES (?,?,?)";
            con = DriverManager.getConnection("jdbc:mysql://localhost/coursesmanagement","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1 , jComboBox1.getSelectedItem().toString());
            pst.setString(2, startDate.getText());
            pst.setString(3, endDate.getText());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null , "Data inserted successfully");
            
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in inserting data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showcourseReport();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

